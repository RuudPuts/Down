import UIKit

extension UIColor {
    
    class func downGreenColor() -> UIColor {
        return UIColor(red:0.25, green:0.75, blue:0.45, alpha:1)
    }
    
    class func downRedColor() -> UIColor {
        return UIColor(red:0.84, green:0.29, blue:0.26, alpha:1)
    }
    
}